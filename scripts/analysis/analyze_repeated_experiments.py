#!/usr/bin/env python3
"""
Analyze repeated gateway-side telemetry experiments for Telemetry_Testbed.

Inputs:
  - run manifest CSV generated by scripts/experiments/run_repeated_experiment.sh
  - GWSTAT logs generated by telemetry_gateway --stats
  - SQLite databases with telemetry_state rows

Outputs:
  - run_summary.csv
  - aggregate_summary.csv
  - gwstat_samples.csv
  - SUMMARY.md
  - optional PNG figures if matplotlib is available

Important measurement note:
The current repository's telemetry_state.seq is a cache update counter, not a
snapshot-publication counter. Therefore, gaps in seq are not interpreted as lost
snapshots. This analyzer reports DB row counts, aggregate throughput, observed
DB-window row-rate, duplicate/decreasing seq indicators, and sequence-span
statistics, but it does not claim packet-loss or replay-completeness proof.
"""

from __future__ import annotations

import argparse
import csv
import math
import os
import re
import sqlite3
import statistics as stats
from collections import defaultdict
from pathlib import Path
from typing import Dict, Iterable, List, Optional, Tuple

GWSTAT_RE = re.compile(
    r"GWSTAT\s+up=(?P<up>[0-9.]+)s\s+"
    r"rx=(?P<rx>[0-9.]+)\s+msg/s\s+"
    r"db=(?P<db>[0-9.]+)\s+row/s\s+"
    r"kbps=(?P<kbps>[0-9.]+)\s+"
    r"avg_payload=(?P<payload>[0-9.]+)B\s+"
    r"avg_commit=(?P<avg_commit>[0-9.]+)ms\s+"
    r"max_commit=(?P<max_commit>[0-9.]+)ms\s+"
    r"qlen=(?P<qlen>[0-9]+)\s+"
    r"qmax=(?P<qmax>[0-9]+)\s+"
    r"batch=(?P<batch>[0-9]+)"
)

NUM_FIELDS = {
    "rep", "rate_hz", "batch", "duration_s", "sensor_rate_hz", "mqtt_port", "ws_port",
    "start_epoch", "end_epoch", "exit_code"
}


def ffloat(x, default=math.nan) -> float:
    try:
        if x is None or x == "":
            return default
        return float(x)
    except Exception:
        return default


def fint(x, default=0) -> int:
    try:
        if x is None or x == "":
            return default
        return int(float(x))
    except Exception:
        return default


def mean(values: Iterable[float]) -> float:
    vals = [float(v) for v in values if v is not None and not math.isnan(float(v))]
    return sum(vals) / len(vals) if vals else math.nan


def stdev(values: Iterable[float]) -> float:
    vals = [float(v) for v in values if v is not None and not math.isnan(float(v))]
    return stats.stdev(vals) if len(vals) >= 2 else 0.0 if len(vals) == 1 else math.nan


def percentile(values: Iterable[float], p: float) -> float:
    vals = sorted(float(v) for v in values if v is not None and not math.isnan(float(v)))
    if not vals:
        return math.nan
    if len(vals) == 1:
        return vals[0]
    k = (len(vals) - 1) * p / 100.0
    lo = math.floor(k)
    hi = math.ceil(k)
    if lo == hi:
        return vals[int(k)]
    return vals[lo] * (hi - k) + vals[hi] * (k - lo)


def fmt(x: float, nd=3) -> str:
    if x is None or (isinstance(x, float) and math.isnan(x)):
        return "NA"
    return f"{x:.{nd}f}"


def read_manifest(path: Path) -> List[Dict[str, str]]:
    with path.open("r", newline="", encoding="utf-8") as f:
        rows = list(csv.DictReader(f))
    return rows


def parse_gwstat_log(path: Path, warmup_s: float = 0.0) -> List[Dict[str, float]]:
    samples: List[Dict[str, float]] = []
    if not path.exists():
        return samples
    with path.open("r", encoding="utf-8", errors="replace") as f:
        for line in f:
            m = GWSTAT_RE.search(line)
            if not m:
                continue
            d = {k: ffloat(v) for k, v in m.groupdict().items()}
            d["qlen"] = fint(m.group("qlen"))
            d["qmax"] = fint(m.group("qmax"))
            d["batch"] = fint(m.group("batch"))
            if d.get("up", 0.0) >= warmup_s:
                samples.append(d)
    return samples


def sqlite_scalar(con: sqlite3.Connection, sql: str, default=None):
    try:
        cur = con.execute(sql)
        row = cur.fetchone()
        return row[0] if row and row[0] is not None else default
    except sqlite3.Error:
        return default


def analyze_db(db_path: Path, nominal_rate_hz: float) -> Dict[str, float]:
    out: Dict[str, float] = {
        "db_exists": 1.0 if db_path.exists() else 0.0,
        "rows": 0.0,
        "db_main_bytes": float(db_path.stat().st_size) if db_path.exists() else 0.0,
        "db_wal_bytes": float(Path(str(db_path) + "-wal").stat().st_size) if Path(str(db_path) + "-wal").exists() else 0.0,
        "db_shm_bytes": float(Path(str(db_path) + "-shm").stat().st_size) if Path(str(db_path) + "-shm").exists() else 0.0,
        "db_total_bytes": 0.0,
        "bytes_per_sample_main": math.nan,
        "bytes_per_sample_total": math.nan,
        "db_window_s": math.nan,
        "db_rate_from_rows": math.nan,
        "expected_rows_db_window": math.nan,
        "expected_minus_rows_db_window": math.nan,
        "seq_min": math.nan,
        "seq_max": math.nan,
        "seq_span": math.nan,
        "seq_distinct": math.nan,
        "seq_duplicate_rows": math.nan,
        "seq_decrease_count": math.nan,
        "seq_equal_adjacent_count": math.nan,
    }
    out["db_total_bytes"] = out["db_main_bytes"] + out["db_wal_bytes"] + out["db_shm_bytes"]
    if not db_path.exists():
        return out

    try:
        con = sqlite3.connect(f"file:{db_path}?mode=ro", uri=True)
    except sqlite3.Error:
        return out

    try:
        rows = float(sqlite_scalar(con, "SELECT COUNT(*) FROM telemetry_state", 0) or 0)
        out["rows"] = rows
        if rows > 0:
            out["bytes_per_sample_main"] = out["db_main_bytes"] / rows if out["db_main_bytes"] else math.nan
            out["bytes_per_sample_total"] = out["db_total_bytes"] / rows if out["db_total_bytes"] else math.nan
            min_ns = sqlite_scalar(con, "SELECT MIN(received_at_ns) FROM telemetry_state", None)
            max_ns = sqlite_scalar(con, "SELECT MAX(received_at_ns) FROM telemetry_state", None)
            if min_ns is not None and max_ns is not None and max_ns > min_ns:
                window_s = (float(max_ns) - float(min_ns)) / 1e9
                out["db_window_s"] = window_s
                out["db_rate_from_rows"] = (rows - 1.0) / window_s if window_s > 0 else math.nan
                expected = round(nominal_rate_hz * window_s) + 1
                out["expected_rows_db_window"] = float(expected)
                out["expected_minus_rows_db_window"] = float(expected) - rows
            out["seq_min"] = float(sqlite_scalar(con, "SELECT MIN(seq) FROM telemetry_state", 0) or 0)
            out["seq_max"] = float(sqlite_scalar(con, "SELECT MAX(seq) FROM telemetry_state", 0) or 0)
            out["seq_span"] = out["seq_max"] - out["seq_min"]
            out["seq_distinct"] = float(sqlite_scalar(con, "SELECT COUNT(DISTINCT seq) FROM telemetry_state", 0) or 0)
            out["seq_duplicate_rows"] = max(0.0, rows - out["seq_distinct"])

            # Order by gateway reception time; detect decreasing or equal adjacent seq.
            seq_decrease = 0
            seq_equal = 0
            prev = None
            for (seq,) in con.execute("SELECT seq FROM telemetry_state ORDER BY received_at_ns, id"):
                if seq is None:
                    continue
                if prev is not None:
                    if seq < prev:
                        seq_decrease += 1
                    if seq == prev:
                        seq_equal += 1
                prev = seq
            out["seq_decrease_count"] = float(seq_decrease)
            out["seq_equal_adjacent_count"] = float(seq_equal)
    finally:
        con.close()
    return out


def summarize_run(manifest_row: Dict[str, str], warmup_s: float) -> Tuple[Dict[str, object], List[Dict[str, object]]]:
    run_id = manifest_row.get("run_id", "")
    gw_log = Path(manifest_row.get("gw_log", ""))
    db_path = Path(manifest_row.get("db_path", ""))
    rate_hz = ffloat(manifest_row.get("rate_hz"), 0.0)
    batch = fint(manifest_row.get("batch"), 0)
    duration_s = ffloat(manifest_row.get("duration_s"), math.nan)

    samples = parse_gwstat_log(gw_log, warmup_s=warmup_s)
    db_metrics = analyze_db(db_path, rate_hz)

    rx_vals = [s["rx"] for s in samples]
    db_vals = [s["db"] for s in samples]
    payload_vals = [s["payload"] for s in samples]
    avg_commit_vals = [s["avg_commit"] for s in samples]
    max_commit_vals = [s["max_commit"] for s in samples]
    qlen_vals = [s["qlen"] for s in samples]
    qmax_vals = [s["qmax"] for s in samples]
    kbps_vals = [s["kbps"] for s in samples]

    end_epoch = ffloat(manifest_row.get("end_epoch"), math.nan)
    start_epoch = ffloat(manifest_row.get("start_epoch"), math.nan)
    wall_s = end_epoch - start_epoch if not math.isnan(end_epoch) and not math.isnan(start_epoch) else math.nan

    summary: Dict[str, object] = {
        "run_id": run_id,
        "kind": manifest_row.get("kind", ""),
        "rep": manifest_row.get("rep", ""),
        "rate_hz": rate_hz,
        "batch": batch,
        "duration_s": duration_s,
        "sensor_rate_hz": ffloat(manifest_row.get("sensor_rate_hz"), math.nan),
        "wall_s": wall_s,
        "exit_code": fint(manifest_row.get("exit_code"), -1),
        "gwstat_samples": len(samples),
        "mean_rx_msg_s": mean(rx_vals),
        "sd_rx_msg_s": stdev(rx_vals),
        "mean_db_row_s": mean(db_vals),
        "sd_db_row_s": stdev(db_vals),
        "mean_rx_minus_db": mean([a - b for a, b in zip(rx_vals, db_vals)]),
        "max_abs_rx_minus_db": max([abs(a - b) for a, b in zip(rx_vals, db_vals)], default=math.nan),
        "mean_payload_b": mean(payload_vals),
        "mean_kbps": mean(kbps_vals),
        "mean_avg_commit_ms": mean(avg_commit_vals),
        "p95_interval_avg_commit_ms": percentile(avg_commit_vals, 95),
        "p99_interval_avg_commit_ms": percentile(avg_commit_vals, 99),
        "max_reported_commit_ms": max(max_commit_vals, default=math.nan),
        "max_qlen": max(qlen_vals, default=math.nan),
        "max_qmax": max(qmax_vals, default=math.nan),
    }
    summary.update(db_metrics)

    sample_rows: List[Dict[str, object]] = []
    for s in samples:
        row = {
            "run_id": run_id,
            "kind": manifest_row.get("kind", ""),
            "rep": manifest_row.get("rep", ""),
            "rate_hz": rate_hz,
            "batch": batch,
            "up_s": s.get("up"),
            "rx_msg_s": s.get("rx"),
            "db_row_s": s.get("db"),
            "kbps": s.get("kbps"),
            "avg_payload_b": s.get("payload"),
            "avg_commit_ms": s.get("avg_commit"),
            "max_commit_ms_cumulative": s.get("max_commit"),
            "qlen": s.get("qlen"),
            "qmax": s.get("qmax"),
        }
        sample_rows.append(row)

    return summary, sample_rows


def write_csv(path: Path, rows: List[Dict[str, object]], fieldnames: Optional[List[str]] = None) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if not fieldnames:
        keys = []
        for r in rows:
            for k in r.keys():
                if k not in keys:
                    keys.append(k)
        fieldnames = keys
    with path.open("w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=fieldnames)
        w.writeheader()
        for r in rows:
            w.writerow(r)


def aggregate(run_rows: List[Dict[str, object]]) -> List[Dict[str, object]]:
    groups: Dict[Tuple[str, float, int], List[Dict[str, object]]] = defaultdict(list)
    for r in run_rows:
        groups[(str(r.get("kind", "")), float(r.get("rate_hz", 0.0)), int(r.get("batch", 0)))].append(r)

    metrics = [
        "mean_rx_msg_s", "mean_db_row_s", "mean_rx_minus_db", "max_abs_rx_minus_db",
        "mean_payload_b", "mean_avg_commit_ms", "p95_interval_avg_commit_ms",
        "p99_interval_avg_commit_ms", "max_reported_commit_ms", "max_qlen", "max_qmax",
        "rows", "db_rate_from_rows", "expected_minus_rows_db_window", "bytes_per_sample_main",
        "bytes_per_sample_total", "seq_duplicate_rows", "seq_decrease_count", "seq_equal_adjacent_count",
    ]
    out: List[Dict[str, object]] = []
    for (kind, rate, batch), rows in sorted(groups.items(), key=lambda x: (x[0][0], x[0][1], x[0][2])):
        a: Dict[str, object] = {"kind": kind, "rate_hz": rate, "batch": batch, "n_runs": len(rows)}
        for m in metrics:
            vals = [ffloat(r.get(m), math.nan) for r in rows]
            a[f"{m}_mean"] = mean(vals)
            a[f"{m}_sd"] = stdev(vals)
            a[f"{m}_min"] = min([v for v in vals if not math.isnan(v)], default=math.nan)
            a[f"{m}_max"] = max([v for v in vals if not math.isnan(v)], default=math.nan)
        out.append(a)
    return out


def write_summary(path: Path, run_rows: List[Dict[str, object]], agg_rows: List[Dict[str, object]], warmup_s: float) -> None:
    short = [a for a in agg_rows if a.get("kind") == "short"]
    long = [a for a in agg_rows if a.get("kind") == "long"]

    lines: List[str] = []
    lines.append("# Repeated Telemetry Experiment Summary")
    lines.append("")
    lines.append(f"Warm-up filter applied to GWSTAT logs: first {warmup_s:.1f} s excluded.")
    lines.append("")
    lines.append("## Important interpretation note")
    lines.append("")
    lines.append("The current `telemetry_state.seq` value is a simulator cache update counter, not a dedicated snapshot-publication counter. Therefore, gaps in `seq` must not be interpreted as packet loss. This analysis reports aggregate RX/DB throughput, DB row counts, DB-window row-rate, adjacent sequence decreases, and adjacent equal sequence counts. A true sample-level loss analysis would require a dedicated monotonically increasing snapshot sequence number.")
    lines.append("")

    def table(rows: List[Dict[str, object]], title: str):
        lines.append(f"## {title}")
        lines.append("")
        if not rows:
            lines.append("No runs found.")
            lines.append("")
            return
        lines.append("| Kind | Rate | Batch | n | RX msg/s | DB row/s | Avg commit ms | p99 interval avg commit ms | Max commit ms | Max qmax | Rows | DB B/sample | Seq decreases |")
        lines.append("|---|---:|---:|---:|---:|---:|---:|---:|---:|---:|---:|---:|---:|")
        for a in rows:
            lines.append(
                f"| {a['kind']} | {fmt(a['rate_hz'],0)} | {a['batch']} | {a['n_runs']} | "
                f"{fmt(a['mean_rx_msg_s_mean'],2)} ± {fmt(a['mean_rx_msg_s_sd'],2)} | "
                f"{fmt(a['mean_db_row_s_mean'],2)} ± {fmt(a['mean_db_row_s_sd'],2)} | "
                f"{fmt(a['mean_avg_commit_ms_mean'],3)} ± {fmt(a['mean_avg_commit_ms_sd'],3)} | "
                f"{fmt(a['p99_interval_avg_commit_ms_mean'],3)} | "
                f"{fmt(a['max_reported_commit_ms_max'],3)} | "
                f"{fmt(a['max_qmax_max'],0)} | "
                f"{fmt(a['rows_mean'],1)} | "
                f"{fmt(a['bytes_per_sample_main_mean'],2)} | "
                f"{fmt(a['seq_decrease_count_max'],0)} |"
            )
        lines.append("")

    table(short, "Short repeated runs")
    table(long, "Extended runs")

    lines.append("## Files")
    lines.append("")
    lines.append("- `run_summary.csv`: one row per run.")
    lines.append("- `aggregate_summary.csv`: grouped statistics by kind/rate/batch.")
    lines.append("- `gwstat_samples.csv`: one row per parsed GWSTAT sample.")
    lines.append("")
    lines.append("## Paper wording caution")
    lines.append("")
    lines.append("Do not claim packet-loss-free transport from RX≈DB alone. Use wording such as: `Across repeated local runs, mean gateway reception and database insertion rates remained matched within [insert tolerance], while no adjacent sequence decreases were observed. Because the current sequence field is not a snapshot counter, sample-level loss detection remains future work unless a dedicated snapshot sequence number is added.`")
    lines.append("")

    path.write_text("\n".join(lines), encoding="utf-8")


def make_plots(outdir: Path, agg_rows: List[Dict[str, object]]) -> None:
    try:
        import matplotlib.pyplot as plt  # type: ignore
    except Exception:
        return

    # Moderately enlarged fonts for conference-paper figures. The previous
    # enlarged-font version was too dominant in the final plots; these defaults
    # keep labels readable while leaving more room for the data. These values can
    # be overridden without editing the script, for example:
    #   PLOT_BASE_FONT=11 PLOT_LABEL_FONT=13 python3 scripts/analysis/analyze_repeated_experiments.py ...
    base_font = int(os.environ.get("PLOT_BASE_FONT", "10"))
    label_font = int(os.environ.get("PLOT_LABEL_FONT", "12"))
    title_font = int(os.environ.get("PLOT_TITLE_FONT", "12"))
    tick_font = int(os.environ.get("PLOT_TICK_FONT", "10"))
    legend_font = int(os.environ.get("PLOT_LEGEND_FONT", "10"))
    fig_w = float(os.environ.get("PLOT_FIG_W", "5.2"))
    fig_h = float(os.environ.get("PLOT_FIG_H", "3.8"))
    dpi = int(os.environ.get("PLOT_DPI", "300"))

    plt.rcParams.update({
        "font.size": base_font,
        "axes.titlesize": title_font,
        "axes.labelsize": label_font,
        "xtick.labelsize": tick_font,
        "ytick.labelsize": tick_font,
        "legend.fontsize": legend_font,
        "figure.titlesize": title_font,
    })

    short = [a for a in agg_rows if a.get("kind") == "short"]
    if not short:
        return

    # One plot per metric. No explicit color/style, to keep default matplotlib styling.
    for batch in sorted(set(int(a["batch"]) for a in short)):
        rows = sorted([a for a in short if int(a["batch"]) == batch], key=lambda x: float(x["rate_hz"]))
        x = [float(a["rate_hz"]) for a in rows]
        y_rx = [float(a["mean_rx_msg_s_mean"]) for a in rows]
        y_db = [float(a["mean_db_row_s_mean"]) for a in rows]
        fig, ax = plt.subplots(figsize=(fig_w, fig_h))
        ax.plot(x, y_rx, marker="o", label="RX msg/s")
        ax.plot(x, y_db, marker="x", label="DB row/s")
        ax.set_xlabel("Configured snapshot rate (Hz)")
        ax.set_ylabel("Throughput (per second)")
        ax.set_title(f"Gateway throughput, batch={batch}")
        ax.legend()
        ax.grid(True)
        fig.tight_layout()
        fig.savefig(outdir / f"throughput_batch_{batch}.png", dpi=dpi, bbox_inches="tight")
        plt.close(fig)

        y_commit = [float(a["mean_avg_commit_ms_mean"]) for a in rows]
        y_commit_sd = [float(a["mean_avg_commit_ms_sd"]) for a in rows]
        fig, ax = plt.subplots(figsize=(fig_w, fig_h))
        ax.errorbar(x, y_commit, yerr=y_commit_sd, marker="o", capsize=4)
        ax.set_xlabel("Configured snapshot rate (Hz)")
        ax.set_ylabel("Mean interval commit time (ms)")
        ax.set_title(f"Commit behavior, batch={batch}")
        ax.grid(True)
        fig.tight_layout()
        fig.savefig(outdir / f"commit_batch_{batch}.png", dpi=dpi, bbox_inches="tight")
        plt.close(fig)


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--manifest", required=True, help="Path to run_manifest.csv")
    ap.add_argument("--outdir", required=True, help="Output directory")
    ap.add_argument("--warmup-s", type=float, default=3.0, help="Exclude GWSTAT samples before this uptime")
    args = ap.parse_args()

    manifest_path = Path(args.manifest)
    outdir = Path(args.outdir)
    outdir.mkdir(parents=True, exist_ok=True)

    rows = read_manifest(manifest_path)
    run_rows: List[Dict[str, object]] = []
    sample_rows: List[Dict[str, object]] = []
    for r in rows:
        s, samples = summarize_run(r, warmup_s=args.warmup_s)
        run_rows.append(s)
        sample_rows.extend(samples)

    agg_rows = aggregate(run_rows)

    write_csv(outdir / "run_summary.csv", run_rows)
    write_csv(outdir / "gwstat_samples.csv", sample_rows)
    write_csv(outdir / "aggregate_summary.csv", agg_rows)
    write_summary(outdir / "SUMMARY.md", run_rows, agg_rows, warmup_s=args.warmup_s)
    make_plots(outdir, agg_rows)

    print(f"Wrote {outdir / 'run_summary.csv'}")
    print(f"Wrote {outdir / 'aggregate_summary.csv'}")
    print(f"Wrote {outdir / 'gwstat_samples.csv'}")
    print(f"Wrote {outdir / 'SUMMARY.md'}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
